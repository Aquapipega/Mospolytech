int main() {
    double num1, num2;
    char operation;
    std::setlocale(LC_ALL, "ru");

    // 1. Получаем от пользователя 2 числа
    std::cout << "Введите первое число: ";
    std::cin >> num1;
    std::cout << "Введите второе число: ";
    std::cin >> num2;

    // 2. Выводим среднее арифметическое
    std::cout << "Среднее арифметическое: " << (num1 + num2) / 2.0 << std::endl;

    // 3. Просим пользователя ввести знак операции
    std::cout << "Введите операцию (+, -, *, /): ";

    // 4. Сохраняем команду в переменную
    std::cin >> operation;

    // 6. Реализуем с использованием switch-case
    switch (operation) {
    case '+':
        std::cout << num1 << " + " << num2 << " = " << num1 + num2 << std::endl;
        break;
    case '-':
        std::cout << num1 << " - " << num2 << " = " << num1 - num2 << std::endl;
        break;
    case '*':
        std::cout << num1 << " * " << num2 << " = " << num1 * num2 << std::endl;
        break;
    case '/':
        if (num2 != 0) {
            std::cout << num1 << " / " << num2 << " = " << num1 / num2 << std::endl;
        }
        else {
            std::cout << "Ошибка: деление на ноль!" << std::endl;
        }
        break;
    default:
        std::cout << "Неверная операция!" << std::endl;
        break;

    }
}

